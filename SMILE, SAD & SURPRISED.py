def happyface() : 
  print("      _____     ")
  print("   .'       '.  ")
  print("  /   o   o   \ ")
  print(" |             |")
  print(" |   \     /   |")
  print("  \   '---'   /")
  print("   '. _____ .'")

h = happyface()
print(h)

def sadface() : 
  print("      _____     ")
  print("   .'       '.  ")
  print("  /   o   o   \ ")
  print(" |   ;     ;   |")
  print(" |     ---     |")
  print("  \  /     \   /")
  print("   '. _____ .'")

s = sadface()
print(s)

